package org.dizitart.no2;

/**
 * An enum to specify a sort order.
 *
 * @author Anindya Chatterjee
 * @since 1.0
 * @see FindOptions#sort(String, SortOrder)
 */
public enum SortOrder {
    /**
     * Ascending sort order.
     */
    Ascending,
    /**
     * Descending sort order.
     */
    Descending
}
